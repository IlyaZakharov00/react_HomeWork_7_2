const createId = () => Math.random().toString(36).slice(2);

export function New(props: PropsWithChildren) {
    return (
        <div className="wrap-item wrap-item-new">
            <span className="label">New!</span>
            {props.children}
        </div>
    )
};

export function Popular(props: PropsWithChildren) {
    return (
        <div className="wrap-item wrap-item-popular">
            <span className="label">Popular!</span>
            {props.children}
        </div>
    )
};

export function Article(props: TItem) {
    return (
        <div className="item item-article">
            <h3><a href="#">{props.title}</a></h3>
            <p className="views">Прочтений: {props.views}</p>
        </div>
    )
};

export function Video(props: TItem) {
    return (
        <div className="item item-video">
            <iframe src={props.url} frameBorder="0" allow="autoplay; encrypted-media" allowFullScreen></iframe>
            <p className="views">Просмотров: {props.views}</p>
        </div>
    )
};

export function List(props: TPropsList) {
    return props.list.map((item) => {
        switch (item.type) {
            case 'video':
                return (
                    <VideoPopularOrNew key={createId()} item = {item}/>
                );

            case 'article':
                return (
                    <ArticlePopularOrNew key={createId()} item = {item}/>
                );
        }
    });
};

